/**
* _isupper - checks if given input is upper or lower case character .
* @c: is the int argument of the function .
* Return: 2 cases :
* return = 0 it is not upper case char .
* return = 1 is an uppper char .
*/

int _isupper(int c)
{
if (c >= 'A' && c <= 'Z')
{
return (1);
}
else
{
return (-0);
}
}
