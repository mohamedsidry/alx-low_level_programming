#include "3-calc.h"



/**
* op_add - calculate addition of a to b .
*
* @a: input argument1 .
* @b: input argument2 .
*
* Return: result of addition .
*/

int op_add(int a, int b)
{
return (a + b);
}


/**
* op_sub - calculate the subtraction of b from a .
*
* @a: input argument1 .
* @b: input argument2 .
*
* Return: result of subtraction .
*/

int op_sub(int a, int b)
{
return (a - b);
}


/**
* op_mul - calculate multiplication of a in b .
*
* @a: input argument1 .
* @b: input argument2 .
* Return: result of multiplication .
*/

int op_mul(int a, int b)
{
return (a * b);
}


/**
* op_div - calculate division .
*
* @a: input argument1 .
* @b: input argument2 .
*
* Return: division arg1 on arg2 .
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - calculate remainder.
*
* @a: input argument1 .
* @b: input argument2 .
* Return: remainder of division arg1 on arg2 .
*/


int op_mod(int a, int b)
{
return (a % b);
}
