#include "3-calc.h"



/**
* op_add - adds arg1 to arg2 .
* @a: input 1 .
* @b: input 2 .
*
* Return: result of operation .
*/

int op_add(int a, int b)
{
return (a + b);
}


/**
* op_mul - multiplay arg1 in arg2 .
* @a: input 1 .
* @b: input 2 .
*
* Return: result of operation .
*/

int op_mul(int a, int b)
{
return (a * b);
}



/**
* op_sub - sub arg2 from arg1 .
* @a: input 1 .
* @b: input 2 .
*
* Return: result of operation .
*/

int op_sub(int a, int b)
{
return (a - b);
}


/**
* op_div - divids arg1 over arg2 .
* @a: input 1 .
* @b: input 2 .
*
* Return: result of operation .
*/


int op_div(int a, int b)
{
return (a / b);
}


/**
* op_mod - divide arg1 over arg2 and return the rest value  .
* @a: input 1 .
* @b: input 2 .
*
* Return: result of operation .
*/



int op_mod(int a, int b)
{
return (a % b);
}
